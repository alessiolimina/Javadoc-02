package it.develhope.javadoc;

/**
 * This class includes the Arithmetic Operators
 * @author alessiolimina
 */

public class ArithmeticOperators {
    /**
     * This method does an addition
     * @param num1 is the first number
     * @param num2 is the second number
     * @return the result of the addition
     */

    public int sum(int num1, int num2) {return (num1 + num2);}

    /**
     * This method does a subtraction
     * @param num1 is the first number
     * @param num2 is the second number
     * @return the result of the subtraction
     */

    public int sub(int num1, int num2){return (num1 - num2);}

    /**
     * This method does a multiplication
     * @param num1 is the first number
     * @param num2 is the second number
     * @return the result of the multiplication
     */

    public int mul(int num1, int num2){return (num1 * num2);};

    /**
     * This method does a division
     * @param num1 is the first number
     * @param num2 is the second number
     * @return the result of the division
     */

    public int div(int num1, int num2){return (num1 / num2);};

}

